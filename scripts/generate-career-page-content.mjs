function verifySourceDocuments(role) {
  for (const document of role.source.documents) {
    const absolutePath = path.join(root, document.docxPath);
    const bytes = readFileSync(absolutePath);
    const digest = createHash("sha256").update(bytes).digest("hex");
    if (digest !== document.sha256) {
      throw new Error(`${role.appSlug}: source research checksum mismatch for ${document.docxPath}`);
    }
  }
}

