function sentence(value) {
  const normalized = value
    .normalize("NFKC")
    .replace(/[يى]/gu, "ی")
    .replace(/ك/gu, "ک")
    .replace(/\s+/gu, " ")
    .replace(/\s+([،؛؟])/gu, "$1")
    .trim();
  if (!normalized) return normalized;
  return /[.؟]$/u.test(normalized) ? normalized : `${normalized}.`;
}

function question(value) {
  return value
    .normalize("NFKC")
    .replace(/[يى]/gu, "ی")
    .replace(/ك/gu, "ک")
    .replace(/\?/gu, "؟")
    .replace(/\s+/gu, " ")
    .replace(/\s+([،؛؟])/gu, "$1")
    .replace(/[.؟]+$/u, "")
    .trim()
    .concat("؟");
}

