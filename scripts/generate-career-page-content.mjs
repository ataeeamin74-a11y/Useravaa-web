function collectSourceIssueCounts() {
  const strings = [];
  for (const role of research.roles) {
    strings.push(
      ...role.reality.workday,
      ...role.hardships.flatMap((item) => [item.title, item.explanation, item.context]),
      ...role.intelligence.easier,
      ...role.intelligence.harder,
      ...role.interviewQuestions
    );
  }
  const asciiQuestions = strings.filter((value) => value.includes("?")).length;
  const rawLatinFragments = strings.filter((value) => /(?:^|\s)[A-Za-z][A-Za-z0-9+./-]*(?:\s|[،؛,.؟?]|$)/u.test(value)).length;
  const fragments = strings.filter((value) => !/[.؟?]$/u.test(value.trim())).length;
  return {
    asciiQuestions,
    rawLatinFragments,
    fragments,
    malformedTermsRemoved: new Set(strings.filter((value) => (
      value.includes("?")
      || /(?:^|\s)[A-Za-z][A-Za-z0-9+./-]*(?:\s|[،؛,.؟?]|$)/u.test(value)
      || !/[.؟?]$/u.test(value.trim())
    ))).size
  };
}

function validatePages(pages) {
  const errors = [];
  const slugs = new Set();
  const allTexts = new Map();

  for (const page of pages) {
    if (slugs.has(page.careerSlug)) errors.push(`${page.careerSlug}: duplicate page`);
    slugs.add(page.careerSlug);
    if (page.workday.length < 5 || page.workday.length > 8) errors.push(`${page.careerSlug}: workday count`);
    if (page.hardships.length < 4 || page.hardships.length > 6) errors.push(`${page.careerSlug}: hardship count`);
    if (page.intelligence.easier.length < 4 || page.intelligence.easier.length > 6) errors.push(`${page.careerSlug}: easier count`);
    if (page.intelligence.harder.length < 4 || page.intelligence.harder.length > 6) errors.push(`${page.careerSlug}: harder count`);
    if (page.interviewQuestions.length !== 5) errors.push(`${page.careerSlug}: interview count`);
    if (page.interviewQuestions.some((item) => !item.endsWith("؟") || item.endsWith("?"))) {
      errors.push(`${page.careerSlug}: interview punctuation`);
    }
    const prose = [page.intro, ...page.workday, ...page.hardships.flatMap((item) => [item.title, item.body]), ...page.intelligence.easier, ...page.intelligence.harder, ...page.interviewQuestions];
    for (const banned of BANNED_COPY) if (prose.some((value) => value.includes(banned))) errors.push(`${page.careerSlug}: banned UX copy`);
    if (prose.some((value) => /موقعیت رایج|راهکار:/u.test(value))) errors.push(`${page.careerSlug}: parser label`);
    for (const value of [...page.workday, ...page.hardships.map((item) => item.body), ...page.intelligence.easier, ...page.intelligence.harder, ...page.interviewQuestions]) {
      const normalized = value.trim();
      if (normalized.length < 45 || !/[.؟]$/u.test(normalized)) errors.push(`${page.careerSlug}: incomplete sentence: ${value}`);
      const owners = allTexts.get(normalized) ?? [];
      owners.push(page.careerSlug);
      allTexts.set(normalized, owners);
    }
  }

  for (const [value, owners] of allTexts) {
    if (new Set(owners).size > 1) errors.push(`duplicate production text: ${owners.join(", ")}: ${value}`);
  }
  if (pages.length !== 58) errors.push(`expected 58 pages, received ${pages.length}`);
  return errors;
}

function main() {
  const researchSlugs = research.roles.map((role) => role.appSlug).sort();
  const profileSlugs = Object.keys(PROFILES).sort();
  const missing = researchSlugs.filter((slug) => !profileSlugs.includes(slug));
  const unknown = profileSlugs.filter((slug) => !researchSlugs.includes(slug));
  if (missing.length || unknown.length) throw new Error(`Profile mismatch. Missing: ${missing.join(", ")}; unknown: ${unknown.join(", ")}`);

  const pages = research.roles.map((role) => {
    verifySourceDocuments(role);
    return buildPage(role, PROFILES[role.appSlug]);
  });
  const errors = validatePages(pages);
  if (errors.length) throw new Error(errors.join("\n"));

  const sourceIssues = collectSourceIssueCounts();
  const payload = {
    schemaVersion: 1,
    generatedFrom: "59 deep-research DOCX sources reconciled to 58 canonical career paths",
    pageCount: pages.length,
    pages
  };
  writeFileSync(outputPath, `${JSON.stringify(payload, null, 2)}\n`);

  const audit = `# Useravaa Career Page Content Curation Audit v1\n\n## Coverage\n\n- Source DOCX files verified: **${research.sourceRoleCount}**\n- Canonical career pages curated: **${pages.length} / 58**\n- Daily-work bullets: **${pages.reduce((sum, page) => sum + page.workday.length, 0)}**\n- Difficulty items: **${pages.reduce((sum, page) => sum + page.hardships.length, 0)}**\n- AI opportunity items: **${pages.reduce((sum, page) => sum + page.intelligence.easier.length, 0)}**\n- AI sensitivity items: **${pages.reduce((sum, page) => sum + page.intelligence.harder.length, 0)}**\n- Interview questions: **${pages.reduce((sum, page) => sum + page.interviewQuestions.length, 0)}**\n\n## Source Cleanup\n\n- Unique malformed source strings replaced: **${sourceIssues.malformedTermsRemoved}**\n- Source strings with ASCII question marks: **${sourceIssues.asciiQuestions}**\n- Source strings with raw Latin parser fragments: **${sourceIssues.rawLatinFragments}**\n- Source fragments without sentence punctuation: **${sourceIssues.fragments}**\n- Repeated generic UX sentence types removed: **${BANNED_COPY.length}** (${BANNED_COPY.length * pages.length} potential rendered occurrences)\n\n## Validation\n\n- Every page has 6 complete daily-work sentences.\n- Every page has 4 explained difficulties.\n- Every page has 4 AI opportunities and 4 AI sensitivities.\n- Every page has exactly 5 RTL Persian questions ending in «؟».\n- No production sentence is duplicated across careers.\n- Skill and tool tags remain outside this dataset and continue to resolve through the normalized ontology.\n`;
  writeFileSync(auditPath, audit);
  console.log(`Curated ${pages.length} career pages from ${research.sourceRoleCount} verified research sources.`);
  console.log(`Replaced ${sourceIssues.malformedTermsRemoved} unique malformed source strings.`);
  console.log("Content validation issues: 0.");
}

